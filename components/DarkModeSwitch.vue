<template>
  <button
    @click="toggleDarkMode"
    class="p-2 text-gray-500 dark:text-gray-400 hover:text-gray-700 dark:hover:text-gray-200 bg-white dark:bg-gray-800 border border-gray-200 dark:border-gray-700 rounded-lg hover:bg-gray-50 dark:hover:bg-gray-700 transition-all duration-200 focus:outline-none focus:ring-2 focus:ring-blue-500 focus:ring-offset-2 dark:focus:ring-offset-gray-900"
    :aria-label="isDark ? 'Switch to light mode' : 'Switch to dark mode'"
  >
    <svg v-if="isDark" style="width: 20px; height: 20px;" fill="none" stroke="currentColor" viewBox="0 0 24 24">
      <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M12 3v1m0 16v1m9-9h-1M4 12H3m15.364 6.364l-.707-.707M6.343 6.343l-.707-.707m12.728 0l-.707.707M6.343 17.657l-.707.707M16 12a4 4 0 11-8 0 4 4 0 018 0z" />
    </svg>
    <svg v-else style="width: 20px; height: 20px;" fill="none" stroke="currentColor" viewBox="0 0 24 24">
      <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M20.354 15.354A9 9 0 018.646 3.646 9.003 9.003 0 0012 21a9.003 9.003 0 008.354-5.646z" />
    </svg>
  </button>
</template>

<script lang="ts" setup>
const isDark = ref(false)

onMounted(() => {
  // Check initial theme from localStorage or system preference
  if (typeof window !== 'undefined') {
    const saved = localStorage.getItem('dark-mode')
    const systemDark = window.matchMedia('(prefers-color-scheme: dark)').matches
    
    if (saved !== null) {
      isDark.value = saved === 'true'
    } else {
      isDark.value = systemDark
    }
    
    applyTheme(isDark.value)
  }
})

function toggleDarkMode() {
  isDark.value = !isDark.value
  applyTheme(isDark.value)
}

function applyTheme(dark: boolean) {
  if (dark) {
    document.documentElement.classList.add('dark')
    localStorage.setItem('dark-mode', 'true')
  } else {
    document.documentElement.classList.remove('dark')
    localStorage.setItem('dark-mode', 'false')
  }
}
</script>